class HousingData {
  final String Id;
  final String image;
  final int amount;
  final String address;
  final int bedrooms;
  final int bathrooms;
  final int area;
  final int garage;
  final String description;

  HousingData({
    required this.Id,
    required this.image,
    required this.amount,
    required this.address,
    required this.bedrooms,
    required this.bathrooms,
    required this.area,
    required this.garage,
    required this.description,
  });
}
